<p>
    The internet is a interesting place, bursting onto the scene some 30 years ago in an explosion of chaos, it was the wild west.
    But almost from the inception a handful of individuals sought to amass as much power in this new medium as possible. At first,
    I think some of these individuals were well intentioned, building stuff for the fun of it and caught up in the rush of having 
    people actually use their stuff. However, as with all things money corrupts and as VCs and private equity signed bigger and 
    bigger cheques the need to amass more users, more clicks, more eyeballs became all-consuming. By the time we releized what 
    was happening we were so enmessed with the digital prisions these early "disrupters" had built that we forgot the web could 
    be a weird and wonderful place. We started to equate these large silos with the internet so whole heartedly that people now complain 
    about the "dead internet" and that the "web isn't fun anymore". 
    <br />
    But these silos aren't the internet; they aren't even the web. There is still a weird and beautiful web out there just waiting to 
    be discovered. You need to remember or learn a pre-Google way of searching, through links, blog rolls and RSS feeds. And you don't 
    have to be a passive consumer, you can build something too. 
    <br /> 
    That's why I've created this little garden. I call it a garden because I intend to tend it over time, I may never update this 
    page again, but I'm sure I'll add others as my intrests and focus shift. This garden is a little bit of a portfolio, it's a 
    bit of an exprement, it's a bit about me, and maybe a bit about the stuff I like, I'm not too sure. It's just my home on the 
    web away from the silos of the 2000s and 2010s. 
</p>

